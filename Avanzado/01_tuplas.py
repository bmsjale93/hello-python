### TUPLAS AVANZADAS ###

# Creando una tupla con tuple()
tupla = tuple(["dato1", "dato2", "dato3", "dato4", "dato5"])
print(tupla)

# Ahora, empaquetamos una tupla otra forma de crear tuplas
tupla2 = "dato1", "dato2", "dato3", "dato4", "dato5"
print(tupla2)

# También podemos crear tuplas sin paréntesis y con solo un dato
tupla3 = "dato1",
print(tupla3)