### OPERADORES ARITMÉTICOS ###

# SUMA Y RESTA
suma = 12 + 5
resta = 12 - 5

# MULTIPLICACIÓN Y DIVISIÓN
multiplicacion = 12 * 5
division = 12 / 5   # Devuelve un float

# EXPONENTES
exponente = 12 ** 5

# DIVISIÓN ENTERA
division_entera = 12 // 5 # Devuelve un entero (Redondea hacia abajo)

# MÓDULO (resto de la división)
modulo = 12 % 5

print(suma) # 17
print(resta) # 7
print(multiplicacion) # 60
print(division) # 2.4
print(exponente) # 248832
print(division_entera) # 2
print(modulo) # 2

